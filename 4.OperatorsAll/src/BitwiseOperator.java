public class Demo3 {
    public static void BitwiseOperator(String[] args) {

        // =====================================================
        // BITWISE OPERATORS
        // =====================================================

        int a = 2;   // Binary: 10
        int b = 3;   // Binary: 11

        // Java internally stores int in 32 bits

        /*
            a = 00000000 00000000 00000000 00000010
            b = 00000000 00000000 00000000 00000011
        */

        // -----------------------------------------------------
        // AND (&)
        // Rule:
        // 1 & 1 = 1
        // otherwise = 0
        // -----------------------------------------------------

        int c = a & b;

        /*
            10
          & 11
          ----
            10  -> 2
         */

        // -----------------------------------------------------
        // OR (|)
        // Rule:
        // If any bit is 1 => result is 1
        // -----------------------------------------------------

        int d = a | b;

        /*
            10
          | 11
          ----
            11 -> 3
         */

        // -----------------------------------------------------
        // XOR (^)
        // Rule:
        // Same bits -> 0
        // Different bits -> 1
        // -----------------------------------------------------

        int e = a ^ b;

        /*
            10
          ^ 11
          ----
            01 -> 1
         */

        // -----------------------------------------------------
        // NOT (~)
        // Rule:
        // Reverse all bits
        // 0 becomes 1
        // 1 becomes 0
        // -----------------------------------------------------

        int f = ~a;

        /*
            a = 00000000 00000000 00000000 00000010

            ~a=

            11111111 11111111 11111111 11111101

            Result = -3

            Shortcut:
            ~n = -(n + 1)

            ~2 = -(2 + 1)
               = -3
         */

        System.out.println(c + " , " + d + " , " + e + " , " + f);


        // =====================================================
        // SHIFT OPERATORS
        // =====================================================

        int g = 1;

        /*
            00000000 00000000 00000000 00000001
         */

        // -----------------------------------------------------
        // LEFT SHIFT (<<)
        // -----------------------------------------------------

        g = g << 33;

        /*
            Important Interview Question

            For int:
            Only last 5 bits are considered

            shift % 32

            33 % 32 = 1

            Therefore:

            g << 33
            == g << 1

            00000001 << 1

            00000010 = 2
         */


        // -----------------------------------------------------
        // BYTE SHIFTING
        // -----------------------------------------------------

        byte h = 1;

        /*
            byte = 8 bits

            00000001
         */

        h = (byte)(h << 1);

        /*
            Step 1:
            00000001 << 1

            Result:
            00000010

            Decimal = 2

            Why cast required?

            byte + operation
            becomes int internally.

            h << 1 returns int.

            Therefore:

            h = (byte)(h << 1);
         */

        System.out.println(g);
        System.out.println(h);
    }
}
